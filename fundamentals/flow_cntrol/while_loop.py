# While Loops in Python 

# ───────────────────────────────
# (1) BASIC WHILE LOOP
count = 1
while count <= 3:
    print("Count:", count)
    count += 1

print("-" * 50)

# ───────────────────────────────
# (2) WHILE LOOP with CONDITION
number = 5
while number > 0:
    print("Number is:", number)
    number -= 2
